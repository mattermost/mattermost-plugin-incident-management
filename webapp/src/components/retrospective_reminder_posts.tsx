interface ReminderCommonProps {
    header: string
    primary: string
    secondary: string
}

const ReminderCommon = (props: ReminderCommonProps) => {
    const incident = useSelector(currentIncident);
    const reminderDuration = incident?.retrospective_reminder_interval_seconds;
    const currentTeam = useSelector(getCurrentTeam);

    let reminderText = (
        <>{'You will not be reminded again.'}</>
    );
    if (reminderDuration !== 0) {
        reminderText = (
            <>
                {'A reminder will be sent in '}
                <b>{renderDuration(moment.duration(reminderDuration, 'seconds'))}</b>
            </>
        );
    }

    return (
        <MessageContainer>
            <Content>
                <Header>
                    {props.header}
                </Header>
                <ButtonRow>
                    <PrimaryButton
                        onClick={() => navigateToUrl(`/${currentTeam.name}/${pluginId}/incidents/${incident?.id}/retrospective`)}
                    >
                        {props.primary}
                    </PrimaryButton>
                    <StyledTertiaryButton
                        onClick={() => noRetrospective(incident?.id)}
                    >
                        {props.secondary}
                    </StyledTertiaryButton>
                </ButtonRow>
                <Divider/>
                <ReminderText>
                    {reminderText}
                </ReminderText>
            </Content>
            <Image/>
        </MessageContainer>
    );
};

